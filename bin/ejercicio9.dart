void main() {
  int a = 10;
  int b = 5;

  int suma = a + b;
  int resta = a - b;
  int multiplicacion = a * b;
  double division = a / b; // La división `/` siempre resulta en un double.

  print("Suma: $suma");
  print("Resta: $resta");
  print("Multiplicación: $multiplicacion");
  print("División: $division");
}
